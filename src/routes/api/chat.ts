import "@tanstack/react-start";
import { createFileRoute } from "@tanstack/react-router";
import { convertToModelMessages, streamText, type UIMessage } from "ai";
import { createLovableAiGatewayProvider } from "@/lib/ai-gateway";

const SYSTEM_PROMPT = `You are Scholaris AI, the in-dashboard assistant for a school analytics app.
You help an admin (Ms. Anita) understand students, attendance, performance, and class trends.

The dashboard has these sections: Dashboard (/), Students (/students), AI Insights (/insights),
Notifications (/notifications). You can suggest the admin navigate there, filter students,
or take actions like sending parent notifications — but you cannot execute those actions yourself yet.

Be concise, friendly, and data-aware. Use markdown for lists. When asked about specific students or
classes, acknowledge you're working from the sample dataset the dashboard shows.`;

export const Route = createFileRoute("/api/chat")({
  server: {
    handlers: {
      POST: async ({ request }: { request: Request }) => {
        const { messages } = (await request.json()) as { messages?: unknown };
        if (!Array.isArray(messages)) {
          return new Response("Messages are required", { status: 400 });
        }

        const key = process.env.LOVABLE_API_KEY;
        if (!key) return new Response("Missing LOVABLE_API_KEY", { status: 500 });

        const gateway = createLovableAiGatewayProvider(key);
        const model = gateway("google/gemini-3-flash-preview");

        const result = streamText({
          model,
          system: SYSTEM_PROMPT,
          messages: await convertToModelMessages(messages as UIMessage[]),
        });

        return result.toUIMessageStreamResponse({
          originalMessages: messages as UIMessage[],
        });
      },
    },
  },
});
